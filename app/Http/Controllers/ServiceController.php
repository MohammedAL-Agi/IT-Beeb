    public function updateService(Request $request)
    {
        // return response()->json($request);
        // return response()->json('backend');
        foreach ($request->service as $val) {

            $data = Service::find($val);

            if ($data) {
                $data->check = 1;
                $data->user_id = $request->user_id;
                $data->save();
            }
        }

        return $request;
    }

